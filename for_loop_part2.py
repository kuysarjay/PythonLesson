# For Loop, loop thru string and Lists.

# Example of a for loop thru string in Python:
word = "Python"
for letter in word:  # Start a for loop that will iterate through each letter in the string "Python".
    print(letter)  # Print each letter on a new line.

# Example of a for loop thru list in Python:
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:  # Start a for loop that will iterate through each item in the list "fruits".
    print(fruit)  # Print each fruit on a new line.